// =============================================================================
// Program.cs — Точка входа в приложение
// =============================================================================
// Стандартная точка входа для Windows Forms приложения на .NET 8.0.
// Создаёт и запускает главную форму (Form1).
// =============================================================================

namespace Prototype
{
    /// Главный класс приложения — содержит метод Main, точку входа.
    internal static class Program
    {
        /// Точка входа в приложение.
        /// Атрибут [STAThread] указывает, что COM-поток должен быть
        /// однопоточным (Single Thread Apartment) — обязательно для WinForms.
        [STAThread]
        static void Main()
        {
            // Инициализация конфигурации приложения
            // (настройки DPI, шрифты по умолчанию и т.д.)
            ApplicationConfiguration.Initialize();

            // Создание и запуск главной формы
            // Application.Run() запускает цикл обработки сообщений Windows
            // и показывает форму Form1 — когда она закрывается, приложение завершается
            Application.Run(new Form1());
        }
    }
}
